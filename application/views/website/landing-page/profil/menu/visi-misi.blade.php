        <h1 style="color:#2971d3">Visi Misi</h1><br>
        <p><strong>&nbsp;</strong><br><strong>Visi Program Studi Teknik Informatika :</strong></p>
        <p><span lang="EN-US">Menghasilkan lulusan yang mandiri, berdaya saing, berbasis nilai dan karakter bangsa serta mampu beradaptasi terhadap perkembangan teknologi informasi menuju program studi unggulan &nbsp;pada tahun 2035</span></p>
        <p><br><br></p>
        <p><strong>Misi Program Studi Teknik Informatika:</strong></p>
        <ol>
            <li>Menyelenggarakan kegiatan pendidikan, penelitian dan pengabdian kepada masyarakat yang berdaya saing dan adaptif terhadap perkembangan teknologi Informasi.</li>
            <li>Melaksanakan kegiatan pendidikan, penelitian dan pengabdian masyarakat berpedoman pada mutu, nilai etika, dan moral akademis.</li>
            <li>Melaksanakan kegiatan yang menghasilkan karya nyata sehingga dapat dinikmati oleh masyarakat melalui pendidikan, penelitian dan pengabdian masyarakat.</li>
            <li>Membangun program kemitraan baik skala nasional maupun internasional untuk mendukung kegiatan pendidikan, penelitian dan pengabdian masyarakat.</li>
            <li>Membangun jiwa kewirausahaan untuk menghasilkan lulusan yang mandiri.</li>
        </ol>
        <p><span><strong>&nbsp;</strong></span></p>
        <p><span><span><strong>&nbsp;</strong></span></span></p>
        <p><span><span><strong>Tujuan Program Studi Teknik Informatika</strong></span></span></p>
        <ol>
            <li>Menghasilkan sarjana Teknik Informatika yang mandiri, kreatif, inovatif terhadap dinamika masyarakat, berwawasan kebangsaan dan&nbsp; mampu menghadapi tantangan global.</li>
            <li>Menghasilkan sarjana yang mempunyai kemampuan di bidang Teknik Informatika bertaraf nasional dan internasional</li>
            <li>Menghasilkan sumberdaya manusia yang memiliki kemampuan penelitian di bidang Teknik Informatika yang bermanfaat bagi kepentingan masyarakat.</li>
            <li>Memperluas jaringan kerjasama guna menciptakan sinergi yang berdaya guna untuk meningkatkan kompetensi di bidang Teknik Informatika</li>
        </ol>
